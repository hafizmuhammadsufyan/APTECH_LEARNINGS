// for loop

for (i = 0; i <= 5; i++) {
    console.log("Hello World", i)
}


// Table example

let num = 15

for (i = 1; i <= 10; i++) {
    console.log(`${num} X ${i} = `, (num * i))
}


// while loop

let count = 1

while (count <= 5) {
    console.log("count = ", count)
    count++
}

// do while loop it performs the taks first then checks the condition example is below


let x = 0

do {
    console.log("X = ", x)
    x++
} while (x <= 3)